#include "../includes/libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[b] != '\0')
	{
		s1[i + b] = s2[b];
		b++;
	}
	s1[i + b] = '\0';
	return (s1);
}
